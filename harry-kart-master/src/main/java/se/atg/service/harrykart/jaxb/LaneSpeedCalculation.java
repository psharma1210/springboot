package se.atg.service.harrykart.jaxb;

public class LaneSpeedCalculation {
	
	int loopNumber;
	int powerSpeed;
	int finalSpeed;
	String horseName;
	public int getLoopNumber() {
		return loopNumber;
	}
	public void setLoopNumber(int loopNumber) {
		this.loopNumber = loopNumber;
	}
	public int getPowerSpeed() {
		return powerSpeed;
	}
	public void setPowerSpeed(int powerSpeed) {
		this.powerSpeed = powerSpeed;
	}
	public int getFinalSpeed() {
		return finalSpeed;
	}
	public void setFinalSpeed(int finalSpeed) {
		this.finalSpeed = finalSpeed;
	}
	public String getHorseName() {
		return horseName;
	}
	public void setHorseName(String horseName) {
		this.horseName = horseName;
	}
	

}
